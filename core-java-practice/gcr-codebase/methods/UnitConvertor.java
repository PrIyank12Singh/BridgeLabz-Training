public class UnitConvertor {

    public static double convertKmToMiles(double kilometers) {
      //formula for converting kilometers to miles   
      return kilometers * 0.621371;
    }

    public static double convertMilesToKm(double miles) {
      //formula for converting miles to kilometers  
      return miles * 1.60934;
    }

    public static double convertMetersToFeet(double meters) {
      //formula for converting meters to feet  
      return meters * 3.28084;
    }

    public static double convertFeetToMeters(double feet) {
      //formula for converting feet to meters  
      return feet * 0.3048;
    }
}
